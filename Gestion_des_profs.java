    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (JOptionPane.showConfirmDialog (null,"confirmer la modification","modification",
                JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {

            stm2.executeUpdate("UPDATE prof SET Nom='"+txtno.getText()+"',prenom='"+txtpr.getText()+
                "',branche='"+txtbr.getSelectedItem().toString()+"',Jour='"+txtnot.getText()+
                "' WHERE id= "+txtid.getText());
            afficher();
        }
        } catch (Exception e){JOptionPane.showMessageDialog(null,"erreur de modifier !!!!!!!"+e.getMessage());
            System.err.println(e);}
    }//GEN-LAST:event_jButton3ActionPerformed
private void afficher(){
         try {
            model1.setRowCount(0);
stm2=conn2.obtenirconnexion().createStatement();
ResultSet Rs=stm2.executeQuery("Select * from prof");
while(Rs.next()){
model1.addRow(new Object[]{Rs.getString("id"),Rs.getString("Nom"),Rs.getString("prenom"),
    Rs.getString("branche"),Rs.getString("Jour")});

}
}catch(Exception e){System.err.println(e);}

tble.setModel(model1);
    }

private void deplace(int i){
        try{
            txtid.setText(model1.getValueAt(i,0).toString());
            txtno.setText(model1.getValueAt(i,1).toString());
            txtpr.setText(model1.getValueAt(i,2).toString());
            txtbr.setSelectedItem(model1.getValueAt(i,3).toString());
            txtnot.setText(model1.getValueAt(i,4).toString());
        
        }catch (Exception e){System.err.println(e);
        JOptionPane.showMessageDialog(null,"erreur de deplacement"+ e.getLocalizedMessage());}
    }
