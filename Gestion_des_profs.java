    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            model1.setRowCount(0);// pour vider la list des client
            {
                Rs2 = stm2.executeQuery("Select * From prof WHERE Jour = '"+txtre.getText()+"'");
            }while (Rs2.next()){

                Object [] prof ={Rs2.getInt(1),Rs2.getString(2),Rs2.getString(3),Rs2.getString(4),Rs2.getString(5)};
                model1.addRow(prof);
            } if (model1.getRowCount () == 0){JOptionPane.showMessageDialog(null,"il y a aucun prof");

            } else{ int i=0;
                deplace(i);
            }

        }catch (Exception e) { System.err.println(e);
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

